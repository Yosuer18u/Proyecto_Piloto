-- VALIDA ACCESO AL MENU
CREATE PROCEDURE [dbo].[SP_L_MENU_VALIDAR_ACCESO]
@USUARIO_ID INT,
@IDENTIFICADOR VARCHAR(100)
AS  
BEGIN  
	SELECT        
	MENUDETALLES.ID, 
	MENUDETALLES.MENU_ID, 
	MENUDETALLES.ROL_ID, 
	MENUDETALLES.PERMITIDO, 
	MENUDETALLES.FECHA_CREACION, 
	MENUDETALLES.ESTADO, 
	MENUS.NOMBRE, 
	MENUS.RUTA, 
	MENUS.IDENTIFICADOR, 
	MENUS.GRUPO, 
