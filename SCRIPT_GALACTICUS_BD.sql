CREATE PROCEDURE SP_CALCULO_IVA_COMPRA
@COMPRA_ID INT 
AS 
BEGIN 
  SELECT 
  SUM(PRODUCTO_PRECIO_COMPRA * CANTIDAD) AS 'Subtotal',
  SUM(PRODUCTO_PRECIO_COMPRA * CANTIDAD)*0.15 AS 'IVA',
