-- LISTA USUARIOS POR SUCURSAL
CREATE PROCEDURE [dbo].[SP_L_USUARIO_BY_SUCURSAL]    
@SUCURSAL_ID INT    
AS    
BEGIN    
SELECT          
USUARIOS.ID,   
USUARIOS.SUCURSAL_ID,   
USUARIOS.CORREO,   
USUARIOS.NOMBRE,   
USUARIOS.APELLIDO,   
USUARIOS.DNI,   
USUARIOS.TELEFONO,   
USUARIOS.CONTRASENIA,  
USUARIOS.IMAGEN, 
