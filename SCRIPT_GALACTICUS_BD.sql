@SUCURSAL_ID INT
AS
BEGIN
	SELECT        
		CATEGORIAS.ID, 
		CATEGORIAS.NOMBRE, 
		SUM(PRODUCTOS.STOCK) AS STOCK
	FROM            
		CATEGORIAS INNER JOIN
		PRODUCTOS ON CATEGORIAS.ID = PRODUCTOS.CATEGORIA_ID
	WHERE        
		CATEGORIAS.SUCURSAL_ID = @SUCURSAL_ID
		AND PRODUCTOS.ESTADO=1
		AND CATEGORIAS.ESTADO=1
	GROUP BY
		CATEGORIAS.ID, 
		CATEGORIAS.NOMBRE
	ORDER BY
		STOCK DESC
END
